package com.pansoft.entity;

public class Paytype {

	private String id;
	private String sq_id;
	private String name;
	private Double price;
	private String unit;
	private String pay_date;
	private String paytypecode;
	private String type;
	private String pay_org;
	private String remark;
	private String ext1;
	private String ext2;
	
	
	public String getPay_org() {
		return pay_org;
	}
	public void setPay_org(String pay_org) {
		this.pay_org = pay_org;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getSq_id() {
		return sq_id;
	}
	public void setSq_id(String sq_id) {
		this.sq_id = sq_id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Double getPrice() {
		return price;
	}
	public void setPrice(Double price) {
		this.price = price;
	}
	public String getUnit() {
		return unit;
	}
	public void setUnit(String unit) {
		this.unit = unit;
	}
	public String getPay_date() {
		return pay_date;
	}
	public void setPay_date(String pay_date) {
		this.pay_date = pay_date;
	}
	public String getPaytypecode() {
		return paytypecode;
	}
	public void setPaytypecode(String paytypecode) {
		this.paytypecode = paytypecode;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	public String getExt1() {
		return ext1;
	}
	public void setExt1(String ext1) {
		this.ext1 = ext1;
	}
	public String getExt2() {
		return ext2;
	}
	public void setExt2(String ext2) {
		this.ext2 = ext2;
	}
	
	
}
